#Tuple in Python:-
#Indexing (position)
a=("Aditya Raj",20,82.6,True)
print("Tuple=",a)
print(type(a))
print("Name=",a[0])
print(type(a[0]))
print("Age=",a[1])
print(type(a[1]))
print("Marks=",a[2])
print(type(a[2]))
print("Statement=",a[3])
print(type(a[3]))
#Basically we use indexing to define the position of multiple types of data in Tuple.
#Indexing always stars from 0.
