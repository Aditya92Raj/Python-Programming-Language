#Dictionary method in python:-
#clear() method is used to delete all key-value pairs.
student={
    "name":"Aditya",
    "age":20,
    "course":"BCA"
}
print("Before used clear()")
print("student=",student)
student.clear()
print("After used clear()")
print("student=",student)
#after used clear method dictionary will empty.
