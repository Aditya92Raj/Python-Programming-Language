# Function in Python:-
# A function is a block of code that performs a specific task.
# In Python, we create a function using def (definition), and we run it by calling it.

# Function Definition
def greet():
    print("Hello, Welcome to Python!")

# Function Call
greet()

# def keyword is used to define a function.
# greet is the name of the function.
# () are parentheses (can contain parameters).
# : indicates the start of the function body.
# greet() is the function call.
